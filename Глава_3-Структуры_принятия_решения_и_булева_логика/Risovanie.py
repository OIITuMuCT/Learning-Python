import turtle

leo = turtle
leo.setup(1024, 768)
leo.showturtle()
leo.speed(10)
leo.forward(200)
leo.left(45)
leo.forward(200)
leo.setheading(90)
leo.forward(200)
leo.left(45)
leo.forward(200)
leo.left(45)
leo.forward(200)
leo.left(45)
leo.forward(200)
leo.left(45)
leo.forward(200)
leo.left(45)
leo.forward(200)
leo.pencolor('red')
leo.pensize(100)
leo.penup()
leo.goto(-35, 100)
leo.pendown()
leo.begin_fill()
e = turtle.numinput("Чего ждем?", "Вадите радиус окружности от 100-250 пикселей", default=190, minval=100, maxval=250)
leo.circle(e)
leo.end_fill()
leo.bgcolor("yellow")
leo.left(90)
leo.pencolor('black')
leo.forward(380)
leo.fillcolor("green")
leo.pencolor('green')
leo.write("Кина не будет!!!")

turtle.done()
